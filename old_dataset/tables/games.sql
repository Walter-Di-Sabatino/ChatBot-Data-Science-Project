CREATE TABLE games (
    app_id VARCHAR(50) PRIMARY KEY,
    name VARCHAR(255),
    release_date DATE,
    estimated_owners VARCHAR(50),
    peak_ccu INT,
    required_age INT,
    price DECIMAL(10, 2),
    dlc_count INT,
    detailed_description TEXT,
    short_description TEXT,
    supported_languages TEXT,
    full_audio_languages TEXT,
    reviews TEXT,
    header_image VARCHAR(500),
    website VARCHAR(500),
    support_url VARCHAR(500),
    support_email VARCHAR(255),
    support_windows BOOLEAN,
    support_mac BOOLEAN,
    support_linux BOOLEAN,
    metacritic_score INT,
    metacritic_url VARCHAR(500),
    user_score INT,
    positive INT,
    negative INT,
    score_rank VARCHAR(50),
    achievements INT,
    recommendations INT,
    notes TEXT,
    average_playtime INT,
    average_playtime_2weeks INT,
    median_playtime INT,
    median_playtime_2weeks INT
);
